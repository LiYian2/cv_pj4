def main():
    args = parse_args()
    internal_cache_root = Path(args.internal_cache_root)
    run_root = Path(args.output_root) if args.output_root else build_default_output_root(internal_cache_root, args.run_key)
    ensure_dir(run_root)
    ensure_dir(run_root / "manifests")
    if args.stage in ("select", "all"):
        stage_select(args, run_root)
    if args.stage in ("difix", "all"):
        stage_difix(args, run_root)
    if args.stage in ("fusion", "all"):
        stage_fusion(args, run_root)
    if args.stage in ("verify", "all"):
        stage_verify(args, run_root)
    if args.stage in ("pack", "all"):
        stage_pack(args, run_root)


if __name__ == "__main__":
    main()

