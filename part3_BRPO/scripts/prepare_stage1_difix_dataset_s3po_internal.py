def canonical_name(frame_id: int) -> str:
    return f"{int(frame_id):05d}.png"


def load_internal_context(internal_cache_root: Path, stage_tag: str):
    manifest = load_json(internal_cache_root / "manifest.json")
    camera_states = load_json(internal_cache_root / "camera_states.json")
    states_by_id = {int(s["frame_id"]): s for s in camera_states}
    stage_meta = load_json(internal_cache_root / stage_tag / "stage_meta.json")
    rendered_non_kf = [int(x) for x in stage_meta["rendered_non_kf_frames"]]
    kf_indices = [int(x) for x in manifest["kf_indices"]]
    return manifest, states_by_id, rendered_non_kf, kf_indices


def pick_frames_between(left_kf: int, right_kf: int, candidates: List[int], placement: str) -> List[Tuple[int, str]]:
    gap = [fid for fid in candidates if left_kf < fid < right_kf]
    if not gap:
        return []
    uniq = sorted(set(gap))
    out: List[Tuple[int, str]] = []

    def nearest(target: float) -> int:
        return min(uniq, key=lambda x: abs(x - target))

    if placement in ("midpoint", "both"):
        out.append((nearest(left_kf + (right_kf - left_kf) / 2.0), "midpoint"))
    if placement in ("tertile", "both"):
        out.append((nearest(left_kf + (right_kf - left_kf) / 3.0), "tertile_left"))
        out.append((nearest(left_kf + 2.0 * (right_kf - left_kf) / 3.0), "tertile_right"))

    dedup = {}
    for fid, label in out:
        dedup.setdefault(fid, label)
    return [(fid, dedup[fid]) for fid in sorted(dedup.keys())]


def infer_neighbors(frame_id: int, kf_indices: List[int]) -> Tuple[Optional[int], Optional[int]]:
    left = max([k for k in kf_indices if k < frame_id], default=None)
    right = min([k for k in kf_indices if k > frame_id], default=None)
    return left, right


def build_record(
    run_root: Path,
    internal_cache_root: Path,
    stage_tag: str,
    frame_id: int,
    placement: str,
    left_ref: int,
    right_ref: int,
    image_name: str,
    *,
    gap_index: Optional[int] = None,
    allocation_rank: Optional[int] = None,
    allocation_policy: Optional[str] = None,
    candidate_fraction: Optional[float] = None,
    selection_score: Optional[float] = None,
    selection_label: Optional[str] = None,
):
    cname = image_name or canonical_name(frame_id)
    return InternalPseudoRecord(
        frame_id=int(frame_id),
        placement=placement,
        left_ref_frame_id=int(left_ref),
        right_ref_frame_id=int(right_ref),
        image_name=cname,
        render_rgb_path=str((internal_cache_root / stage_tag / "render_rgb" / f"{int(frame_id)}_pred.png").resolve()),
        render_depth_path=str((internal_cache_root / stage_tag / "render_depth_npy" / f"{int(frame_id)}_pred.npy").resolve()),
        render_input_path=str((run_root / "inputs" / "raw_render" / cname).resolve()),
        left_ref_input_path=str((run_root / "inputs" / "left_ref" / cname).resolve()),
        right_ref_input_path=str((run_root / "inputs" / "right_ref" / cname).resolve()),
        left_fixed_path=str((run_root / "difix" / "left_fixed" / cname).resolve()),
        right_fixed_path=str((run_root / "difix" / "right_fixed" / cname).resolve()),
        gap_index=None if gap_index is None else int(gap_index),
        allocation_rank=None if allocation_rank is None else int(allocation_rank),
        allocation_policy=allocation_policy,
        candidate_fraction=None if candidate_fraction is None else float(candidate_fraction),
        selection_score=None if selection_score is None else float(selection_score),
        selection_label=selection_label,
    )


def build_records_from_selection_manifest(
    selection_manifest_path: Path,
    run_root: Path,
    internal_cache_root: Path,
    stage_tag: str,
    states_by_id: Dict[int, Dict],
    kf_indices: List[int],
) -> List[InternalPseudoRecord]:
    payload = load_json(selection_manifest_path)
    items = payload.get("records", payload) if isinstance(payload, dict) else payload
    records: List[InternalPseudoRecord] = []
    for item in items:
        frame_id = int(item["frame_id"])
        left_ref = item.get("left_ref_frame_id")
        right_ref = item.get("right_ref_frame_id")
        if left_ref is None or right_ref is None:
            left_ref, right_ref = infer_neighbors(frame_id, kf_indices)
        if left_ref is None or right_ref is None:
            raise ValueError(f"selection manifest frame_id={frame_id} missing left/right KF neighbor")
        state = states_by_id[frame_id]
        placement = item.get("placement") or item.get("selection_label") or "selection_manifest"
        records.append(
            build_record(
                run_root,
                internal_cache_root,
                stage_tag,
                frame_id,
                placement,
                int(left_ref),
                int(right_ref),
                item.get("image_name", state.get("image_name", canonical_name(frame_id))),
                gap_index=item.get("gap_index"),
                allocation_rank=item.get("allocation_rank", item.get("selection_rank")),
                allocation_policy=item.get("allocation_policy"),
                candidate_fraction=item.get("candidate_fraction"),
                selection_score=item.get("selection_score"),
                selection_label=item.get("selection_label"),
            )
        )
    return records


def stage_select(args, run_root: Path):
    internal_cache_root = Path(args.internal_cache_root)
    manifest, states_by_id, rendered_non_kf, kf_indices = load_internal_context(internal_cache_root, args.stage_tag)

    records: List[InternalPseudoRecord] = []
    selection_manifest_path = Path(args.selection_manifest).resolve() if args.selection_manifest else None
    if selection_manifest_path:
        records = build_records_from_selection_manifest(
            selection_manifest_path,
            run_root,
            internal_cache_root,
            args.stage_tag,
            states_by_id,
            kf_indices,
        )
    elif args.frame_ids:
        for fid in args.frame_ids:
            left, right = infer_neighbors(int(fid), kf_indices)
            if left is None or right is None:
                raise ValueError(f"frame_id={fid} missing left/right KF neighbor")
            st = states_by_id[int(fid)]
            records.append(build_record(run_root, internal_cache_root, args.stage_tag, int(fid), "explicit", left, right, st.get("image_name", canonical_name(fid))))
    else:
        for gap_idx, (left_kf, right_kf) in enumerate(zip(kf_indices[:-1], kf_indices[1:])):
            picks = pick_frames_between(left_kf, right_kf, rendered_non_kf, args.placement)
            for rank_in_gap, (fid, label) in enumerate(picks, start=1):
                st = states_by_id[int(fid)]
                records.append(
                    build_record(
                        run_root,
                        internal_cache_root,
                        args.stage_tag,
                        int(fid),
                        label,
                        left_kf,
                        right_kf,
                        st.get("image_name", canonical_name(fid)),
                        gap_index=gap_idx,
                        allocation_rank=rank_in_gap,
                        allocation_policy=args.placement,
                    )
                )

    records = sorted(records, key=lambda x: ((x.gap_index if x.gap_index is not None else 10**9), (x.allocation_rank if x.allocation_rank is not None else 10**9), x.frame_id))
    if args.limit is not None:
        records = records[: args.limit]

    for rec in records:
        if not args.dry_run:
            symlink_force(Path(rec.render_rgb_path), Path(rec.render_input_path))
            symlink_force(Path(states_by_id[rec.left_ref_frame_id]["image_path"]), Path(rec.left_ref_input_path))
            symlink_force(Path(states_by_id[rec.right_ref_frame_id]["image_path"]), Path(rec.right_ref_input_path))

    unique_gap_ids = sorted({int(r.gap_index) for r in records if r.gap_index is not None})
    gap_counts = {}
    for rec in records:
        gap_key = str(rec.gap_index) if rec.gap_index is not None else "ungrouped"
        gap_counts[gap_key] = gap_counts.get(gap_key, 0) + 1

    write_json(run_root / "manifests" / "pseudo_selection_manifest.json", [asdict(r) for r in records])
    write_json(run_root / "manifests" / "source_manifest.json", {
        "scene_name": args.scene_name,
        "run_key": args.run_key,
        "stage_tag": args.stage_tag,
        "internal_cache_root": str(internal_cache_root.resolve()),
        "kf_indices": kf_indices,
        "rendered_non_kf_frames": rendered_non_kf,
        "num_frames": manifest["num_frames"],
        "num_kf": len(kf_indices),
        "num_non_kf": len(rendered_non_kf),
        "placement": args.placement,
        "frame_ids_override": args.frame_ids,
        "selection_manifest": str(selection_manifest_path) if selection_manifest_path else None,
        "num_selected": len(records),
        "num_unique_gaps": len(unique_gap_ids),
        "gap_allocation_counts": gap_counts,
    })
    summary = {
        "scene_name": args.scene_name,
        "run_key": args.run_key,
        "stage_tag": args.stage_tag,
        "placement": args.placement if not args.frame_ids else "explicit",
        "selection_source": "selection_manifest" if selection_manifest_path else ("frame_ids" if args.frame_ids else "placement"),
        "selection_manifest": str(selection_manifest_path) if selection_manifest_path else None,
        "num_selected": len(records),
        "num_unique_gaps": len(unique_gap_ids),
        "gap_allocation_counts": gap_counts,
        "selected_frame_ids": [r.frame_id for r in records],
    }
    write_json(run_root / "manifests" / "selection_summary.json", summary)
    print(json.dumps(summary, indent=2, ensure_ascii=False))


def load_internal_records(run_root: Path) -> List[InternalPseudoRecord]:
    data = load_json(run_root / "manifests" / "pseudo_selection_manifest.json")
    return [InternalPseudoRecord(**x) for x in data]


def stage_difix(args, run_root: Path):
    from prepare_stage1_difix_dataset_s3po import load_difix_model, run_single_difix

    records = load_internal_records(run_root)
    if args.dry_run:
        print(f"[dry-run] would run difix on {len(records)} internal pseudo frames")
        return

    model = load_difix_model(args.difix_model_name, args.difix_model_path, args.timestep)
    ensure_dir(run_root / "difix" / "left_fixed")
    ensure_dir(run_root / "difix" / "right_fixed")
    for idx, rec in enumerate(records, start=1):
        print(f"[{idx}/{len(records)}] frame_id={rec.frame_id} placement={rec.placement}")
        run_single_difix(
            model_bundle=model,
            image_path=Path(rec.render_input_path),
            ref_path=Path(rec.left_ref_input_path),
            output_path=Path(rec.left_fixed_path),
            prompt=args.prompt,
            height=args.height,
            width=args.width,
            overwrite=args.overwrite,
        )
        run_single_difix(
            model_bundle=model,
            image_path=Path(rec.render_input_path),
            ref_path=Path(rec.right_ref_input_path),
            output_path=Path(rec.right_fixed_path),
            prompt=args.prompt,
            height=args.height,
            width=args.width,
            overwrite=args.overwrite,
        )
    write_json(run_root / "manifests" / "difix_manifest.json", {
        "prompt": args.prompt,
        "height": args.height,
        "width": args.width,
        "timestep": args.timestep,
        "difix_model_name": args.difix_model_name,
        "num_records": len(records),
        "frame_ids": [r.frame_id for r in records],
    })


def resolve_fixed_path(root: Optional[str], image_name: str) -> Optional[Path]:
    if not root:
        return None
    p = Path(root) / image_name
    return p if p.exists() else None


def default_fusion_root(run_root: Path) -> Path:
    return run_root / "fusion" / "samples"


def fusion_frame_root(run_root: Path, frame_id: int) -> Path:
    return default_fusion_root(run_root) / str(int(frame_id))


def stage_fusion(args, run_root: Path):
    import torch
    from munch import munchify
    from utils.config_utils import load_config
    from utils.external_eval_utils import load_gaussians_from_ply
    from pseudo_branch.observation.brpo_reprojection_verify import render_depth_from_state
    from pseudo_branch.observation.pseudo_fusion import run_fusion_for_sample

    internal_cache_root = Path(args.internal_cache_root)
    manifest, states_by_id, _, _ = load_internal_context(internal_cache_root, args.stage_tag)

    config = load_config(str(internal_cache_root.parent / 'config.yml'))
    pipe = munchify(config['pipeline_params'])
    stage_ply = internal_cache_root / args.stage_tag / 'point_cloud' / 'point_cloud.ply'
    gaussians = load_gaussians_from_ply(config, str(stage_ply))
    background = torch.tensor(manifest['background'], dtype=torch.float32, device='cuda')

    records = load_internal_records(run_root)
    fusion_root = default_fusion_root(run_root)
    ensure_dir(fusion_root)
    fusion_samples = []

    exact_backend_root = None
    if args.fusion_use_exact_backend_confidence and args.fusion_exact_backend_root:
        exact_backend_root = Path(args.fusion_exact_backend_root)

    for rec in records:
        frame_root = fusion_frame_root(run_root, rec.frame_id)
        ensure_dir(frame_root)

        render_rgb = Path(rec.render_rgb_path)
        render_depth_path = Path(rec.render_depth_path)
        if not render_rgb.exists():
            raise FileNotFoundError(f"Missing render RGB for fusion: {render_rgb}")
        if not render_depth_path.exists():
            raise FileNotFoundError(f"Missing render depth for fusion: {render_depth_path}")

        left_fixed = resolve_fixed_path(args.left_fixed_root, rec.image_name)
        right_fixed = resolve_fixed_path(args.right_fixed_root, rec.image_name)
        left_fixed = left_fixed or Path(rec.left_fixed_path)
        right_fixed = right_fixed or Path(rec.right_fixed_path)

        if args.target_rgb_source == "difix":
            if not left_fixed.exists() or not right_fixed.exists():
                raise FileNotFoundError(f"Missing difix outputs for fusion image_name={rec.image_name}")
            target_left = left_fixed
            target_right = right_fixed
            fusion_input_mode = "difix_left_right"
        else:
            target_left = render_rgb
            target_right = render_rgb
            fusion_input_mode = "render_placeholder"

        pseudo_state = states_by_id[int(rec.frame_id)]
        left_ref_state = states_by_id[int(rec.left_ref_frame_id)]
        right_ref_state = states_by_id[int(rec.right_ref_frame_id)]
        render_depth = np.load(render_depth_path).astype(np.float32)
        left_ref_depth = render_depth_from_state(gaussians, left_ref_state, pipe, background)
        right_ref_depth = render_depth_from_state(gaussians, right_ref_state, pipe, background)

        exact_conf_left = None
        exact_conf_right = None
        if exact_backend_root is not None:
            exact_frame_root = exact_backend_root / f"frame_{int(rec.frame_id):04d}"
            l_conf = exact_frame_root / "confidence_left_exact.npy"
            r_conf = exact_frame_root / "confidence_right_exact.npy"
            if l_conf.exists() and r_conf.exists():
                exact_conf_left = np.load(l_conf).astype(np.float32)
                exact_conf_right = np.load(r_conf).astype(np.float32)

        stats, *_ = run_fusion_for_sample(
            render_rgb_path=str(render_rgb),
            target_rgb_left_path=str(target_left),
            target_rgb_right_path=str(target_right),
            depth_left=None,
            conf_left=None,
            depth_right=None,
            conf_right=None,
            render_depth=render_depth,
            output_dir=str(frame_root),
            pseudo_state=pseudo_state,
            left_ref_state=left_ref_state,
            right_ref_state=right_ref_state,
            left_ref_depth=left_ref_depth,
            right_ref_depth=right_ref_depth,
            depth_consistency_tau=0.15,
            translation_scale_tau=1.0,
            overlap_valid_eps=1e-4,
            exact_conf_left=exact_conf_left,
            exact_conf_right=exact_conf_right,
        )
        meta_path = frame_root / 'fusion_meta.json'
        meta = load_json(meta_path) if meta_path.exists() else {}
        meta.update({
            'frame_id': int(rec.frame_id),
            'image_name': rec.image_name,
            'fusion_mode': 'brpo_overlap_confidence_v1',
            'fusion_input_mode': fusion_input_mode,
            'render_rgb_path': str(render_rgb.resolve()),
            'render_depth_path': str(render_depth_path.resolve()),
            'target_rgb_left_path': str(target_left.resolve()),
            'target_rgb_right_path': str(target_right.resolve()),
            'left_ref_frame_id': int(rec.left_ref_frame_id),
            'right_ref_frame_id': int(rec.right_ref_frame_id),
            'left_ref_rgb_path': str(Path(left_ref_state['image_path']).resolve()),
            'right_ref_rgb_path': str(Path(right_ref_state['image_path']).resolve()),
            'stage_ply_path': str(stage_ply.resolve()),
            'fusion_use_exact_backend_confidence': bool(args.fusion_use_exact_backend_confidence),
            'fusion_exact_backend_root': str(exact_backend_root.resolve()) if exact_backend_root is not None else None,
        })
        write_json(meta_path, meta)
        fusion_samples.append({
            'frame_id': int(rec.frame_id),
            'frame_root': str(frame_root.resolve()),
            'target_rgb_fused_path': str((frame_root / 'target_rgb_fused.png').resolve()),
            'confidence_mask_fused_path': str((frame_root / 'confidence_mask_fused.npy').resolve()),
            'fusion_meta_path': str(meta_path.resolve()),
            'support_ratio_fused': meta.get('support_ratio_fused', stats.get('support_ratio_fused')),
        })

    write_json(run_root / 'manifests' / 'fusion_manifest.json', {
        'fusion_root': str(fusion_root.resolve()),
        'fusion_mode': 'brpo_overlap_confidence_v1',
        'target_rgb_source': args.target_rgb_source,
        'stage_ply_path': str(stage_ply.resolve()),
        'num_records': len(records),
        'frame_ids': [r.frame_id for r in records],
        'samples': fusion_samples,
    })


