        brpo_paths = {}
        if not args.dry_run:
            brpo_paths = maybe_link_brpo_artifacts(sample_dir, effective_brpo_root, rec.frame_id, args.strict_brpo_mask)
            ensure_dir(sample_dir / "diag")

