        if rec.allocation_rank is not None:
            source_meta["allocation_rank"] = int(rec.allocation_rank)
        if rec.allocation_policy is not None:
            source_meta["allocation_policy"] = rec.allocation_policy
        if rec.candidate_fraction is not None:
            source_meta["candidate_fraction"] = float(rec.candidate_fraction)
        if rec.selection_score is not None:
            source_meta["selection_score"] = float(rec.selection_score)
        if rec.selection_label is not None:
            source_meta["selection_label"] = rec.selection_label
        if not args.dry_run:
            write_json(sample_dir / "camera.json", camera)
            write_json(sample_dir / "refs.json", refs)

